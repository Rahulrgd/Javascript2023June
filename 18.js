// else if example

let temperature = 18;

if(temperature < 0){
    console.log("It's too cold outside.");
}else if(temperature < 16){
    console.log("It's cold outside.");
}else if(temperature < 25){
    console.log("Temperature is ok outside.");
}else if(temperature < 35){
    console.log("Let's go to swim outside");
}else if(temperature < 45){
    console.log("Turn on the AC!");
}else{
    console.log("It's too hot out side!");
}